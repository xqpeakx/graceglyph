import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import ts from "typescript";

const ROOT = resolve(dirname(new URL(import.meta.url).pathname), "..");
const INDEX_PATH = resolve(ROOT, "src/index.ts");
const OUTPUT_PATH = resolve(ROOT, "docs/site/api-reference.md");

function main() {
  const exportsByModule = parseRootExports(readFileSync(INDEX_PATH, "utf8"));
  const docsByModule = new Map();
  for (const [modulePath, symbols] of exportsByModule) {
    const sourcePath = resolveSourcePath(modulePath);
    if (!sourcePath) {
      docsByModule.set(
        modulePath,
        symbols.map((name) => ({ name, summary: "" })),
      );
      continue;
    }
    const symbolDocs = collectSymbolDocs(sourcePath);
    const rows = symbols.map((name) => ({
      name,
      summary: symbolDocs.get(name) ?? "",
    }));
    docsByModule.set(modulePath, rows);
  }
  const markdown = renderMarkdown(docsByModule);
  writeFileSync(OUTPUT_PATH, markdown, "utf8");
  // eslint-disable-next-line no-console
  console.log(`wrote ${OUTPUT_PATH}`);
}

function parseRootExports(source) {
  const byModule = new Map();
  const namedPattern = /export\s+(?:type\s+)?\{([\s\S]*?)\}\s+from\s+"([^"]+)";/g;
  const starPattern = /export\s+\*\s+from\s+"([^"]+)";/g;

  let match;
  while ((match = namedPattern.exec(source)) !== null) {
    const names = splitSpecifiers(match[1] ?? "");
    const modulePath = match[2];
    const current = byModule.get(modulePath) ?? [];
    for (const entry of names) if (!current.includes(entry)) current.push(entry);
    byModule.set(modulePath, current);
  }

  while ((match = starPattern.exec(source)) !== null) {
    const modulePath = match[1];
    const sourcePath = resolveSourcePath(modulePath);
    const exported = sourcePath ? collectAllExportNames(sourcePath) : [];
    if (exported.length === 0) continue;
    const current = byModule.get(modulePath) ?? [];
    for (const name of exported) if (!current.includes(name)) current.push(name);
    byModule.set(modulePath, current);
  }

  return byModule;
}

function splitSpecifiers(block) {
  return block
    .split(",")
    .map((part) => part.trim())
    .filter((part) => part.length > 0)
    .map((entry) => {
      const asParts = entry.split(/\s+as\s+/i);
      return asParts.length > 1 ? asParts[1].trim() : asParts[0].trim();
    });
}

function resolveSourcePath(modulePath) {
  const withoutJs = modulePath.replace(/\.js$/, "");
  const candidates = [
    resolve(ROOT, "src", `${withoutJs.slice(2)}.ts`),
    resolve(ROOT, "src", `${withoutJs.slice(2)}.tsx`),
  ];
  for (const candidate of candidates) {
    if (existsSync(candidate)) return candidate;
  }
  return undefined;
}

function collectAllExportNames(path) {
  const map = collectSymbolDocs(path);
  return [...map.keys()].sort((a, b) => a.localeCompare(b));
}

function collectSymbolDocs(path) {
  const text = readFileSync(path, "utf8");
  const source = ts.createSourceFile(path, text, ts.ScriptTarget.Latest, true);
  const docs = new Map();

  for (const statement of source.statements) {
    if (!hasExportModifier(statement)) continue;
    if (ts.isFunctionDeclaration(statement) && statement.name) {
      docs.set(statement.name.text, summaryForNode(statement));
      continue;
    }
    if (
      (ts.isInterfaceDeclaration(statement) ||
        ts.isTypeAliasDeclaration(statement) ||
        ts.isClassDeclaration(statement) ||
        ts.isEnumDeclaration(statement)) &&
      statement.name
    ) {
      docs.set(statement.name.text, summaryForNode(statement));
      continue;
    }
    if (ts.isVariableStatement(statement)) {
      for (const decl of statement.declarationList.declarations) {
        if (ts.isIdentifier(decl.name)) docs.set(decl.name.text, summaryForNode(statement));
      }
    }
  }

  return docs;
}

function hasExportModifier(node) {
  return !!node.modifiers?.some((m) => m.kind === ts.SyntaxKind.ExportKeyword);
}

function summaryForNode(node) {
  const docs = node.jsDoc;
  if (!Array.isArray(docs) || docs.length === 0) return "";
  const first = docs[0];
  const comment = normalizeComment(first.comment);
  if (!comment) return "";
  const collapsed = comment.replace(/\s+/g, " ").trim();
  if (collapsed.length === 0) return "";
  const period = collapsed.indexOf(".");
  return period > 0 ? collapsed.slice(0, period + 1) : collapsed;
}

function normalizeComment(comment) {
  if (!comment) return "";
  if (typeof comment === "string") return comment;
  if (Array.isArray(comment)) {
    return comment
      .map((part) => (typeof part.text === "string" ? part.text : ""))
      .join(" ")
      .trim();
  }
  return "";
}

function renderMarkdown(docsByModule) {
  const lines = [
    "# API Reference",
    "",
    "<!-- This file is auto-generated by scripts/generate-api-reference.mjs -->",
    "",
    "This reference is generated from public exports in `src/index.ts` and",
    "TSDoc comments from source modules.",
    "",
    "Regenerate with:",
    "",
    "```bash",
    "npm run docs:api",
    "```",
    "",
  ];

  for (const modulePath of [...docsByModule.keys()].sort((a, b) => a.localeCompare(b))) {
    lines.push(`## \`${modulePath}\``);
    lines.push("");
    const symbols = docsByModule.get(modulePath) ?? [];
    for (const symbol of symbols.sort((a, b) => a.name.localeCompare(b.name))) {
      const summary = symbol.summary.length > 0 ? symbol.summary : "No TSDoc summary available.";
      lines.push(`- \`${symbol.name}\` - ${summary}`);
    }
    lines.push("");
  }

  return `${lines.join("\n").trimEnd()}\n`;
}

main();
